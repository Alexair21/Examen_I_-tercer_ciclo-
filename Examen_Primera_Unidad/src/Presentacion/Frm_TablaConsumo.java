    private void Bt_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_buscarActionPerformed
        ConsumoCliente c=null;
         int cont = 0;
        String Dato = txt_codigo.getText();
        for (int i = 0; i < consumos.size(); i++) {
            if (Dato.equals(consumos.get(i).getCodigo())) {
                cont = i;
                c = new ConsumoCliente(consumos.get(i).getCodigo(), 
                        consumos.get(i).getMedida(), consumos.get(i).getPeriodo(),
                        consumos.get(i).getMonto_pago());

            }
            if (c == null) {
                cont = -1;
            }
        }
      
            if (cont != -1) {
                JOptionPane.showMessageDialog(null, "Usuario encontrado");
                  for (int i = 0; i < consumos.size(); i++) {
                c = new ConsumoCliente(consumos.get(i).getCodigo(), consumos.get(i).getMedida(),
                        consumos.get(i).getPeriodo(), consumos.get(i).getMonto_pago());
                JOptionPane.showMessageDialog(null, "Codigo: " + consumos.get(i).getCodigo()
                        + "\n Medidas: " + consumos.get(i).getMedida()
                        + "\n Periodo: " + consumos.get(i).getPeriodo()
                        + "\n Valor de conexion S/: " + consumos.get(i).getMonto_pago(), "Usuario", 1);
            }
                  } else {
                JOptionPane.showMessageDialog(null, "Usuario no encontrado", "No encontrado", 0);

            }
        
        
    }//GEN-LAST:event_Bt_buscarActionPerformed

